class ArithmeticOperators{
    public static void main(String args[]){
        int a = 10,b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(++a);
        System.out.println(++b);
        System.out.println(--a);
        System.out.println(--b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(true && false);
        System.out.println(true || false);
        System.out.println(!true);
        System.out.println(a = b);
        System.out.println(a += b);
        System.out.println(a -= b);
        System.out.println(a *= b);
        System.out.println(a /= b);

    }
}